package com.example.backend.dto;

import java.time.LocalDate;

// DTO to receive allocation request from client
public class AllocationRequest {

    private Long employeeId; // ID of employee

    private Long projectId; // ID of project

    private LocalDate startDate; // allocation start date

    private LocalDate endDate; // allocation end date

    private int percentage; // allocation percentage

    // Getters and setters

    public Long getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(Long employeeId) {
        this.employeeId = employeeId;
    }

    public Long getProjectId() {
        return projectId;
    }

    public void setProjectId(Long projectId) {
        this.projectId = projectId;
    }

    public LocalDate getStartDate() {
        return startDate;
    }

    public void setStartDate(LocalDate startDate) {
        this.startDate = startDate;
    }

    public LocalDate getEndDate() {
        return endDate;
    }

    public void setEndDate(LocalDate endDate) {
        this.endDate = endDate;
    }

    public int getPercentage() {
        return percentage;
    }

    public void setPercentage(int percentage) {
        this.percentage = percentage;
    }
}